@Composable
fun BottomBar(
    navController: NavHostController
) {
    val items = listOf(
        BottomNavItem.Config,
        BottomNavItem.Serve,
        BottomNavItem.Message
    )

    val navBackStackEntry by navController.currentBackStackEntryAsState()
    val currentRoute = navBackStackEntry?.destination?.route

    NavigationBar {
        items.forEach { item ->
            val selected = currentRoute == item.route

            NavigationBarItem(
                selected = selected,
                onClick = {
                    navController.navigate(item.route) {
                        popUpTo(navController.graph.startDestinationId) {
                            saveState = true
                        }
                        launchSingleTop = true
                        restoreState = true
                    }
                },
                icon = {
                    val iconId = if (currentRoute == item.route) item.selectedIcon else item.unselectedIcon
                    Icon(
                        painter = painterResource(id = iconId), // Usa painterResource para Drawables
                        contentDescription = item.label,
                        modifier = Modifier.size(24.dp) // Ajuste o tamanho conforme necess√°rio
                    )
                },
                label = { Text(item.label) }
            )
        }
    }
}